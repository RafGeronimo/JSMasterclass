// Operações de math:

console.log(Math.abs(10)); //converte o sinal do número para positivo
console.log(Math.abs(-10)); //converte o sinal do número para positivo
console.log(Math.ceil(1.1)); //arredonda o numero para CIMA: 2
console.log(Math.ceil(-1.1)); // arredonda o numero para CIMA: -1
console.log(Math.floor(9.9)); // arredonda o numero para BAIXO: 9
console.log(Math.floor(-9.9)); //arredonda o numero para BAIXO: -10
console.log(Math.round(4.5)); //arredonda para cima de 0.5 a 0.9: 5
console.log(Math.round(-4.5)); // arredonda para cima: -4
console.log(Math.sign(5)); //retorna 1 (sinal do 5)
console.log(Math.sign(-5)); //retorna -1 (sinal do -5)
console.log(Math.trunc(2.3)); //elimina a parte decimal do numero, tornando-o inteiro
console.log(Math.trunc(-2.3)); //Elimina a parte decimal do numero.
