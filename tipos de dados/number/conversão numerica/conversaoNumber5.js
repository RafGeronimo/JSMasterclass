//Utilizando parseInt para converter strings em numeros de base (num, base)
console.log(parseInt("10", 10)); // padrão ja é base 10 (nao precisaria colocar)
console.log(parseInt("9.9", 10)); //parte inteira apenas
console.log(parseInt("A", 16)); // parse int de A na base 16 = 10
console.log(parseInt("11", 2)); // 11 na base 2 = 3
console.log(parseInt("010", 8)); //"010" na base 8 = 8